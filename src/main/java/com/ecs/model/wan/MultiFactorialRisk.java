package com.ecs.model.wan;

import java.util.List;

public class MultiFactorialRisk {
    private List<String> riskName;

    private List<String> riskValue;

    public List<String> getRiskName() {
        return riskName;
    }

    public void setRiskName(List<String> riskName) {
        this.riskName = riskName;
    }

    public List<String> getRiskValue() {
        return riskValue;
    }

    public void setRiskValue(List<String> riskValue) {
        this.riskValue = riskValue;
    }
}
